// Declare four variables
let a = 1;
let b = 2;
let c = 4;
let d = 3;

// Addition operation
let sum = a + b;
console.log("Sum of a and b:", sum); // Output: 3

// Subtraction
let difference = c - d;
console.log("Difference between c and d:", difference); // Output: 1

// Multiplication
let product = a * b;
console.log("Product of a and b:", product); // Output: 2

// Division
let quotient = c / b;
console.log("Quotient of c divided by b:", quotient); // Output: 2

// Modulus (remainder of a divided by b)
let remainder = a % b;
console.log("Remainder of a divided by b:", remainder); // Output: 1

// Exponentiation (a raised to the power of d)
let power = a ** c;
console.log("a raised to the power of c:", power); // Output: 1

// Harder operations to prove functionality
let hardOperation = (a + c) * (d - b);
console.log("Result of the complex operation (a + c) * (d - b):", hardOperation); // Output: 5

// Incrementing a variable
a++;
console.log("Value of a after incrementing:", a); // Output: 2

// Decrementing a variable
b--;
console.log("Value of d after decrementing:", d); // Output: 1
